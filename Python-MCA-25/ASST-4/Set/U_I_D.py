# union(), intersection(), difference() with other sets.
set_A = {1, 2, 3, 4, 5}
set_B = {1, 3, 5, 7, 8}

#union
print(f"Union of set A, B : {set_A.union(set_B)}")

#intersection
print(f"Intersection of set A, B : {set_A.intersection(set_B)}")

#difference
print(f"Difference of set A, B : {set_A.difference(set_B)}")